// Buscar valor por ano
export const getValorPorAno = (item, ano, campo = 'unitario') => {
  const suffix = ano === 2024 ? '2024' : ano === 2022 ? '2022' : '2021';
  return item[`${campo}${suffix}`];
};

// Buscar item por ID
export const getItemById = (id) => {
  return TABELA_CUSTOS.find(item => item.id === id);
};

// Filtrar por categoria
export const getItensPorCategoria = (categoria) => {
  return TABELA_CUSTOS.filter(item => item.categoria === categoria);
};

// Filtrar por subcategoria
export const getItensPorSubcategoria = (categoria, subcategoria) => {
  return TABELA_CUSTOS.filter(
    item => item.categoria === categoria && item.subcategoria === subcategoria
  );
};

// Buscar item por texto (pesquisa)
export const buscarItens = (texto) => {
  const textoLower = texto.toLowerCase();
  return TABELA_CUSTOS.filter(item =>
    item.tipo.toLowerCase().includes(textoLower) ||
    item.categoria.toLowerCase().includes(textoLower) ||
    item.subcategoria.toLowerCase().includes(textoLower)
  );
};

// Formatar valor para exibição (2 casas decimais)
export const formatarValor = (valor) => {
  if (valor === null || valor === undefined || isNaN(valor)) return '0,00';
  return valor.toFixed(2).replace('.', ',');
};

// Obter todas as categorias únicas
export const getCategorias = () => {
  const categorias = [...new Set(TABELA_CUSTOS.map(item => item.categoria))];
  return categorias.filter(c => c); // Remove vazios
};

// Obter subcategorias de uma categoria
export const getSubcategorias = (categoria) => {
  const subcategorias = [...new Set(
    TABELA_CUSTOS
      .filter(item => item.categoria === categoria)
      .map(item => item.subcategoria)
  )];
  return subcategorias.filter(s => s); // Remove vazios
};